namespace Laboration.GameResources.Constants
{
	// Contains constant values for singular and plural forms used in the game.
	// These constants are used to correctly display the number of guesses in both singular and plural forms.
	public static class Plurals
	{
		// Singular form of the word 'guess'.
		// Used when referring to a single guess made by the player.
		public const string GuessSingular = "guess";

		// Plural form of the word 'guess'.
		// Used when referring to multiple guesses made by the player.
		public const string GuessPlural = "guesses";
	}
}